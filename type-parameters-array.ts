/**
 * Using type parameters for array
 * Format: Array<T>: `T` is a placeholder for type
 * Type parameters specify the type that the array can use
 */

let popularCars: Array<string>;
popularCars = ['Mazda R2', 'StonisGT', 'Camera'];
alert(popularCars);

let popularCarColors = new Array<string>(4);
popularCarColors = ['red', 'stealth black', 'tourquise', ''];
alert(popularCarColors);
